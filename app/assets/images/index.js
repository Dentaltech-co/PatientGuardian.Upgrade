export const getCreditCardImg = (cardType) => {
    switch (cardType && cardType.toLowerCase()){
        case "visa":
            return Visa
        case "mastercard":
            return MasterCard
        case "american express":
            return AmericanExpress
        case "discover":
            return Discover
        case "jcb":
            return JCB
        case "unionpay":
            return UnionPay
        case "diners club":
            return DinersClub
        default:
            return Visa
    }
}

